    private void lst_balozokMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lst_balozokMousePressed
        //A bálozók listájára kattintva, ha gólya a kiválasztott illető,
        //akkor „válasszon” egy véletlen zenét az adott kínálatból
        //(nyilván nem a másik listára való kattintással, hanem véletlen generálással,
        if (lst_balozok.getSelectedValue() != null) {
            Balozok balozok = (Balozok) lst_balozok.getSelectedValue();
            if (balozok.getEvFolyam().contains("1")) {
                lbl_kivansag.setText(balozok.getNev() + " kívánsága:");
                ZeneSzam zeneSzam;
                int veletlenZene;
                veletlenZene = (int) (Math.random() * zeneszamModell.size());
                zeneSzam = zeneszamModell.get(veletlenZene);

                ((Golya) balozok).kivalaszt(zeneSzam);

                valasztottZeneModell.clear();
                for (ZeneSzam szam : ((Golya) balozok).getZeneLista()) {
                    valasztottZeneModell.addElement(szam);
                }

                lst_kivansag.setModel(valasztottZeneModell);
                lst_kivansag.repaint();
            } else {
                lbl_kivansag.setText("");
                valasztottZeneModell.clear();

                lst_kivansag.setModel(valasztottZeneModell);
            }
        }

    }//GEN-LAST:event_lst_balozokMousePressed

