    private void rendez() {
        List<Balozok> balozokList = Collections.list(balozokModell.elements());
        Collections.sort(balozokList);
        Collections.sort(balozokList, new Rendezes());
        balozokModell.clear();
        for (Balozok balozok : balozokList) {
            balozokModell.addElement(balozok);
        }
    }
