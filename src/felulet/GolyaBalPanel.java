    private void btn_mutatasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_mutatasActionPerformed
        lbl_tancolok.setVisible(true);
        int fogyasztas = (int) (Math.random() * (Global.FELSO_FOGYASZTAS - Global.ALSO_FOGYASZTAS + 1) + Global.ALSO_FOGYASZTAS);

        int tancoloIndex = (int) (Math.random() * balozokModell.size()); 
        Balozok tancolo = balozokModell.get(tancoloIndex);
        tancolo.tancol();

        int penzkoltoIndex = (int) (Math.random() * balozokModell.size()); 
        Balozok penzkolto = balozokModell.get(penzkoltoIndex); 
        penzkolto.fogyaszt(fogyasztas);

        tancModell.clear();

        int max = balozokModell.get(0).getTancainakSzama();

        for (int i = 0; i < balozokModell.size(); i++) {
            if (balozokModell.get(i).getTancainakSzama() > max) {
                max = balozokModell.get(i).getTancainakSzama();
            }
        }

        for (int i = 0; i < balozokModell.size(); i++) {
            if (balozokModell.get(i).getTancainakSzama() == max) {
                tancModell.addElement(balozokModell.get(i));
            }
        }

        lst_legtobbettancolok.setModel(tancModell);
        lst_legtobbettancolok.repaint();
        lst_balozok.repaint();
    }//GEN-LAST:event_btn_mutatasActionPerformed

    private void beolvasBalozok() {
        InputStream ins = this.getClass().getResourceAsStream(utvonal1);
        Scanner scan = new Scanner(ins, CHAR_SET);
        String sor;
        String[] adatok;

        while (scan.hasNextLine()) {
            sor = scan.nextLine();
            adatok = sor.split(";");
            if (adatok[1].equals("1")) {
                balozo = new Golya(adatok[0], adatok[1]);
            } else {
                balozo = new Balozok(adatok[0], adatok[1]);
            }

            balozo.zsebPenz(((int) (Math.random() * (Global.FELSO_ERTEK - Global.ALSO_ERTEK + 1) + Global.ALSO_ERTEK)));
            balozokModell.addElement(balozo);
        }

        lst_balozok.setModel(balozokModell);

    }

    private void beolvasZenek() {
        InputStream ins = this.getClass().getResourceAsStream(utvonal2);
        Scanner scan = new Scanner(ins, CHAR_SET);
        String sor;
        String[] adatok;
        ZeneSzam zeneSzam;
        while (scan.hasNextLine()) {
            sor = scan.nextLine();
            adatok = sor.split(";");
            zeneSzam = new ZeneSzam(adatok[0], adatok[1]);
            zeneszamModell.addElement(zeneSzam);

        }
        lst_zeneszam.setModel(zeneszamModell);
    }


